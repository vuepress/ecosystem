/**
 * PhotoSwipe behavior options
 *
 * PhotoSwipe 行为选项
 */
export interface PhotoSwipeBehaviorOptions {
  /**
   * Whether to enable download button
   *
   * 是否启用下载按钮
   */
  download?: boolean

  /**
   * Whether to enable fullscreen button
   *
   * 是否启用全屏按钮
   */
  fullscreen?: boolean

  /**
   * Whether to close when scrolling
   *
   * 是否在滚动时关闭
   */
  scrollToClose?: boolean
}
