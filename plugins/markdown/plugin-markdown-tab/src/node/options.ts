/**
 * markdown tab plugin configuration
 */
export interface MarkdownTabPluginOptions {
  /**
   * Whether to enable code tabs.
   *
   * 是否启用代码组。
   *
   * @default false
   */
  codeTabs?: boolean

  /**
   * Whether to enable tabs.
   *
   * 是否启用标签页分组。
   *
   * @default false
   */
  tabs?: boolean
}
