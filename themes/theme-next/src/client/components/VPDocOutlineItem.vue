<script setup lang="ts">
import type { MenuItem } from '../composables/outline.js'

const { headers, root = false } = defineProps<{
  /**
   * Outline headers
   */
  headers: MenuItem[]
  /**
   * If is root
   */
  root?: boolean
}>()

const onClick = ({ target: el }: Event): void => {
  const id = (el as HTMLAnchorElement).href.split('#')[1]
  const heading = document.getElementById(decodeURIComponent(id))
  heading?.focus({ preventScroll: true })
}
</script>

<template>
  <ul class="vp-doc-outline-item" :class="root ? 'root' : 'nested'">
    <li v-for="{ children, link, title } in headers" :key="link">
      <a class="outline-link" :href="link" :title="title" @click="onClick">{{
        title
      }}</a>
      <template v-if="children?.length">
        <VPDocOutlineItem :headers="children" />
      </template>
    </li>
  </ul>
</template>

<style scoped>
.root {
  position: relative;
  z-index: 1;
}

.nested {
  padding-right: 16px;
  padding-left: 16px;
}

.outline-link {
  display: block;

  overflow: hidden;

  color: var(--vp-c-text-mute);

  font-weight: 400;
  font-size: 14px;
  line-height: 32px;
  text-overflow: ellipsis;
  white-space: nowrap;

  transition: color 0.5s;
}

.outline-link:hover,
.outline-link.active {
  color: var(--vp-c-text);
  transition: color 0.25s;
}

.outline-link.nested {
  padding-left: 13px;
}
</style>
